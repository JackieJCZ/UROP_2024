% this script colors each triangle of the mesh with different colors
% taken from the first of june Color Palette
% https://www.color-hex.com/color-palette/37560
% or
% https://www.color-hex.com/color-palette/1034879

function   colorMesh(p,t)
%colours
% C = [155,73,180;...
%     58,123,154;...
%     212,167,58;...
%     81,150,46];

%https://www.color-hex.com/color-palette/1034879

% C = [238,204,99;...
%     241,96,35;...
%     173,224,28;...
%     68,222,234;...
%     26,118,169];

% https://www.color-hex.com/color-palette/895
C = [206,150,176;...
    255,238,173;...
    255,111,105;...
    255,204,92;...
    136,216,176;...
    150,176,206];
% transform the colors to rgb \in [0,1]
C = C/255;

for i = 1 : length(t)
    tmp = fill(p(1,t(1:3,i)),p(2,t(1:3,i)),C(mod(i,size(C,1))+1,:));
    set(tmp,'LineWidth',0.05);hold on;
end



